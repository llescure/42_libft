char	*ft_strchr(const char *str, int c)
{
	int i;

	i = 0;
	if (c == '\0')
		return ((char *)str);
	while (str[i] != '\0')
	{
		if (c == str[i])
		{
			return ((char *)&str[i]);
		}
		i++;
	}
	return (0);
}
